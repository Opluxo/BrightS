const int chinese_glyph_count = sizeof(chinese_glyphs) / sizeof(chinese_glyph_t);

uint32_t brights_utf8_to_codepoint(const char *str, int *len)
{
    if (!str || !len) return 0;
    
    unsigned char c = (unsigned char)str[0];
    
    if (c < 0x80) {
        *len = 1;
        return c;
    }
    
    if ((c & 0xE0) == 0xC0 && str[1]) {
        *len = 2;
        return ((uint32_t)(c & 0x1F) << 6) | (unsigned char)(str[1] & 0x3F);
    }
    
    if ((c & 0xF0) == 0xE0 && str[1] && str[2]) {
        *len = 3;
        return ((uint32_t)(c & 0x0F) << 12) | 
               ((uint32_t)(str[1] & 0x3F) << 6) | 
               (unsigned char)(str[2] & 0x3F);
    }
    
    if ((c & 0xF8) == 0xF0 && str[1] && str[2] && str[3]) {
        *len = 4;
        return ((uint32_t)(c & 0x07) << 18) | 
               ((uint32_t)(str[1] & 0x3F) << 12) |
               ((uint32_t)(str[2] & 0x3F) << 6) | 
               (unsigned char)(str[3] & 0x3F);
    }
    
    *len = 1;
    return c;
}

const uint8_t* brights_get_chinese_glyph(uint32_t codepoint)
{
    for (int i = 0; i < chinese_glyph_count; i++) {
        if (chinese_glyphs[i].codepoint == codepoint) {
            return chinese_glyphs[i].bitmap;
        }
    }
    return NULL;
}

int brights_font_char_width(char c)
{
    (void)c;
    return FONT_WIDTH;
}

int brights_font_string_width(const char *str)
{
    if (!str) return 0;
    
    int width = 0;
    int pos = 0;
    
    while (str[pos]) {
        int len;
        uint32_t cp = brights_utf8_to_codepoint(str + pos, &len);
        (void)cp;
        width += FONT_WIDTH;
        pos += len;
    }
    
    return width;
}

void brights_font_draw_char(int x, int y, char c, uint32_t fg_color, uint32_t bg_color)
{
    if (!brights_fb_available()) return;
    
    brights_fb_info_t *fb = brights_fb_get_info();
    if (!fb) return;
    
    uint32_t *framebuffer = (uint32_t *)fb->framebuffer;
    uint32_t stride = fb->pitch / 4;
    
    const uint8_t *glyph = brights_font_8x16[(unsigned char)c];
    
    for (int row = 0; row < 16; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            int px = x + (7 - col);
            int py = y + row;
            if (px >= 0 && px < (int)fb->width && py >= 0 && py < (int)fb->height) {
                if (bits & (1 << col)) {
                    framebuffer[py * stride + px] = fg_color;
                } else if (bg_color != 0xFFFFFFFF) {
                    framebuffer[py * stride + px] = bg_color;
                }
            }
        }
    }
    
    if (bg_color != 0xFFFFFFFF) {
        for (int row = 0; row < 16; row++) {
            for (int col = 8; col < 16; col++) {
                int px = x + col;
                int py = y + row;
                if (px >= 0 && px < (int)fb->width && py >= 0 && py < (int)fb->height) {
                    framebuffer[py * stride + px] = bg_color;
                }
            }
        }
    }
}

void brights_font_draw_string(int x, int y, const char *str, uint32_t fg_color, uint32_t bg_color)
{
    if (!str) return;
    
    int cx = x;
    int pos = 0;
    
    while (str[pos]) {
        int len;
        uint32_t cp = brights_utf8_to_codepoint(str + pos, &len);
        
        if (cp < 0x80) {
            brights_font_draw_char(cx, y, (char)cp, fg_color, bg_color);
        } else {
            const uint8_t *glyph = brights_get_chinese_glyph(cp);
            if (glyph) {
                brights_fb_info_t *fb = brights_fb_get_info();
                if (fb) {
                    uint32_t *framebuffer = (uint32_t *)fb->framebuffer;
                    uint32_t stride = fb->pitch / 4;
                    
                    for (int row = 0; row < 16; row++) {
                        uint8_t row_data0 = glyph[row * 2];
                        uint8_t row_data1 = glyph[row * 2 + 1];
                        
                        for (int col = 0; col < 16; col++) {
                            int byte_idx = col / 8;
                            int bit_idx = 7 - (col % 8);
                            uint8_t bits = (byte_idx == 0) ? row_data0 : row_data1;
                            
                            int px = cx + col;
                            int py = y + row;
                            
                            if (px >= 0 && px < (int)fb->width && py >= 0 && py < (int)fb->height) {
                                if (bits & (1 << bit_idx)) {
                                    framebuffer[py * stride + px] = fg_color;
                                } else if (bg_color != 0xFFFFFFFF) {
                                    framebuffer[py * stride + px] = bg_color;
                                }
                            }
                        }
                    }
                }
            } else {
                brights_font_draw_char(cx, y, '?', fg_color, bg_color);
            }
        }
        
        cx += FONT_WIDTH;
        pos += len;
    }
}
